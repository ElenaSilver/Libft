#include "libft.h"

size_t	ft_nextch(char *str)
{
	size_t i;

	i = 0;
	while ((str[i] == ' ' || str[i] == '\n' || str[i] == '\t')
		&& str[i] != '\0')
		i++;
	if (str[i] == '\0')
		return (0);
	return (0);
}
